package earthquakes;

public class EqSearch {
	private int distance;
	private int minmag;

	EqSearch() {
	}

	public int getDistance() {
		return distance;
	}

	public void setDistance(int x) {
		distance = x;
	}

	public int getMinmag() {
		return minmag;
	}

	public void setMinmag(int x) {
		minmag = x;
	}

}